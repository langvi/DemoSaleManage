    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String tenNVTim = txtTenNVTim.getText();
        String sdtTim = txtSDTTim.getText();
        String queryFind = null;
        if(tenNVTim.trim().isEmpty() && sdtTim.trim().isEmpty()){
            dataEmployee.callData(queryData, tableEmployee);
        }
        else if(sdtTim.trim().isEmpty()){
            queryFind = "SELECT * FROM employee WHERE employee_name = ? ;";
        }
        else if(tenNVTim.trim().isEmpty()){
            queryFind = "SELECT * FROM employee WHERE employee_phone_number = ? ;";
        }
        else{
            queryFind = "SELECT * FROM employee WHERE employee_name = ? OR employee_phone_number = ?;";
        }
        EmployeeController model = new EmployeeController();
        model.callSearchData(queryFind, tableEmployee, tenNVTim, txtSDTTim.getText());
    }//GEN-LAST:event_btnSearchActionPerformed

