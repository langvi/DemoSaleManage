    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tableEmployee.getModel();
        int row = tableEmployee.getSelectedRow();
        String IDrow = (this.tableEmployee.getModel().getValueAt(row, 0).toString()); // lay ra dia chi của dong hien tai
        int res = JOptionPane.showConfirmDialog(this,"Xóa Người Này Khỏi Danh Sách ?","",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
        if (res == JOptionPane.YES_OPTION){
            EmployeeController empModel = new EmployeeController();
            empModel.DeleteEmployee(Integer.parseInt(IDrow));
            empModel.callData(queryData, tableEmployee);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

